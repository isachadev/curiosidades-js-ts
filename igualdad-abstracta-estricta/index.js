/**
 * Igualdad abstracta y estricta
 */

console.log("123 == '123':", 123 == "123");
console.log("123 === '123':", 123 === "123");

console.log("false == '':", false == "");
console.log("false === '':", false === "");

console.log("null == undefined:", null == undefined);
console.log("null === undefined:", null === undefined);

console.log("0 == false:", 0 == false);
console.log("0 === false:", 0 === false);
